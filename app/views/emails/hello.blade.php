<p>So sorry to hear your bike was stolen.  This sucks.  But you're doing all you can to get it back - that's all you can do.</p>

<p>So you've submitted your bike on the Blacklist, but if you want to invest a little more time, you can multiply the chance of your bike coming back to you:</p>

<h1>With 1.5 Minutes</h1>
<p>You can let everyone you know that your bike is missing:</p>

<p>1. Copy this text:</p>

<code>
Help!  My bike has been stolen!  Will you keep an eye out for it?
{{ $url }}
</code>

<p>2. Go to <a href="http://Facebook.com" target="_blank">facebook.com</a> and paste it.</p>

<p>3. Go to <a href="http://twitter.com">Twitter</a> and paste it.</p>

<h1>With 3 More Minutes</h1>
<p>You can let the biking community know your bike is missing.</p>

<p>1. Join this facebook group: <a href="https://www.facebook.com/groups/146048678917990/">Slow Roll</a></p>
<p>2. Write a short message, you can use this as a template (add your thanks!):</p>

<code>
Hey, my bike got stolen! Can you keep an eye out for it?
{{ $url }}	
</code>

<h1>With 6 more minutes</h1>
<p>You can let the local bike shops know your bike is missing:</p>

<code>
Hi, my bike went missing, if you come across it can you let me know?
{{ $url }}
</code>

<p>Customize this message, add your thanks, talk about why you care about getting the bike back, make it personal.</p>

